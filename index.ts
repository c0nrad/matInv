import { Matrix } from './matrix'

console.log("Trace:")
let m1 = new Matrix(3)
m1.load([-1, 0, 3, 11, 5, 2, 6, 12, -5])
console.log("let A = ")
m1.pprint()
console.log("tr(A) = ", m1.trace())
console.log("\nMul:")

let m2 = new Matrix(2)
let m3 = new Matrix(2)
m2.load([1, 2, 3, 4])
m3.load([2, 0, 1, 2])
let m4 = m2.mul(m3)
let m5 = m3.mul(m2)

console.log("Let A = ")
m2.pprint()
console.log("Let B = ")
m3.pprint()
console.log("A x B = ")
m4.pprint()
console.log("B x A = ")
m5.pprint()

console.log("\nPow:")
let m6 = new Matrix(2)
m6.load([1, 2, 3, 4])
